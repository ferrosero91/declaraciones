export function calculateDueDate(cedula: string): string {
  // Obtener los últimos dos dígitos de la cédula
  const lastTwoDigits = cedula.slice(-2).padStart(2, "0")

  // Buscar la fecha en el calendario
  const dueDate = TAX_CALENDAR[lastTwoDigits as keyof typeof TAX_CALENDAR]

  if (!dueDate) {
    // Si no se encuentra, asignar una fecha por defecto
    return "24-10-2025"
  }

  return dueDate
}

export function getLastTwoDigits(cedula: string): string {
  return cedula.slice(-2).padStart(2, "0")
}

